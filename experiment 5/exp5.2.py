# List
lst = [1, 2, 3]
print("List elements:")
for i in lst:
    print(i)

# Tuple
tup = (4, 5, 6)
print("\nTuple elements:")
for i in tup:
    print(i)

# Dictionary
dic = {"a": 10, "b": 20}
print("\nDictionary elements:")
for key, value in dic.items():
    print(key, value)

# Set
st = {7, 8, 9}
print("\nSet elements:")
for i in st:
    print(i)
